package com.jslhrd.exam02;

public class MathEx_01 {
	public static void main(String[] args) {
		System.out.println("Math.max(10,50) :" + Math.max(100.4, 50));
		int n = Math.abs(5-7);
		System.out.println("Math.abs(5-7) :" + n);
		System.out.println("Math.sqrt(2) :" + Math.sqrt(2));
		System.out.println("Math.pow(2^3) :" + Math.pow(2, 3));
		System.out.println("Math.round(7/2) :" + Math.round(7/2.));
		System.out.println("Math.floor(7/2) :" + Math.floor(7/2.));
	}

}
